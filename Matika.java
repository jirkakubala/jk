public class Matika {

    public  static  int secist(int x, int y ) {
        return x+y;
    }

    public static  int odecist(int x, int y ) {
        return x-y;
    }

    public static  int vynasobit(int x, int y ) {
        return x*y;
    }

    //pridan dalsi radek...

    public static  int umocnit(int x) {
        return x*x;
    }

    public static double vydelit(double x, double y ) {
        return x/y;
    }

}
